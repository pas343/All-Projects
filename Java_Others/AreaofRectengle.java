public class AreaofRectengle
{
	public static void main(String args[])
	{
		int width = 10;
		int length = 5;

		int perimeter = 2 * width + 2 * length;
		int area = length * width;

		System.out.println("The perimeter of rectengle is " + perimeter + " and area is " + area + ".");
	}
}

